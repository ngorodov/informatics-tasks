# Задача 8: «Анализ заказов».
# Использование множеств для анализа материалов трёх подрядчиков.

# 1. Списки материалов трёх подрядчиков.
supplier1 = ["Кирпич", "Цемент", "Песок", "Доска", "Гвозди"]
supplier2 = ["Кирпич", "Песок", "Щебень", "Рубероид", "Бетон"]
supplier3 = ["Кирпич", "Цемент", "Бетон", "Арматура", "Доска"]

# 2. Преобразуем списки в множества для операций анализа.
set1 = set(supplier1)
set2 = set(supplier2)
set3 = set(supplier3)

# 3. Выводим исходные данные.
print("=" * 60)
print("АНАЛИЗ ЗАКАЗОВ ПОДРЯДЧИКОВ")
print("=" * 60)
print(f"Подрядчик 1: {supplier1}")
print(f"Подрядчик 2: {supplier2}")
print(f"Подрядчик 3: {supplier3}")
print("-" * 60)

# 4. Все уникальные материалы (объединение множеств).
all_unique = set1 | set2 | set3
print(f"1. Все уникальные материалы ({len(all_unique)} шт.):")
print(f"   {sorted(all_unique)}")

# 5. Общие для всех трёх подрядчиков (пересечение).
common_all = set1 & set2 & set3
print(f"\n2. Общие для всех трёх ({len(common_all)} шт.):")
print(f"   {sorted(common_all)}")

# 6. Материалы только у первого подрядчика.
only_first = set1 - (set2 | set3)
print(f"\n3. Только у первого подрядчика ({len(only_first)} шт.):")
print(f"   {sorted(only_first)}")

# 7. Материалы, которые встречаются ровно у двух подрядчиков.
in_two = ((set1 & set2) | (set1 & set3) | (set2 & set3)) - (set1 & set2 & set3)
print(f"\n4. Ровно у двух подрядчиков ({len(in_two)} шт.):")
print(f"   {sorted(in_two)}")

print("=" * 60)
