# Задача 6: «Каталог материалов».
# Работа со списками: индексация, срезы, добавление и удаление элементов.

# 1. Создаём список из 5 материалов.
materials = ["Кирпич", "Цемент", "Песок", "Доска", "Гвозди"]

# 2. Заголовок отчёта.
print("=" * 50)
print("КАТАЛОГ МАТЕРИАЛОВ")
print("=" * 50)

# 3. Выводим первый, последний и средние элементы.
print("Исходный список:", materials)
print("-" * 50)
print(f"Первый элемент: {materials[0]}")
print(f"Последний элемент: {materials[-1]}")
print(f"Средние элементы: {materials[1:4]}")
print(f"Срез [1:-1]: {materials[1:-1]}")

# 4. Добавляем 2 новых материала.
materials.append("Щебень")
materials.append("Рубероид")
print("-" * 50)
print(f"После добавления: {materials}")

# 5. Удаляем второй элемент (индекс 1).
removed = materials.pop(1)
print(f"Удалён элемент: {removed}")

# 6. Выводим итоговый список и его длину.
print("-" * 50)
print(f"Итоговый список: {materials}")
print(f"Длина списка: {len(materials)} элементов")
print("=" * 50)
