# Задача 2: «Параметры помещения».
# Расчёт геометрических параметров помещения и стоимости покраски стен.

# 1. Исходные размеры помещения (в метрах).
length = 6.5  # Длина комнаты
width = 4.2  # Ширина комнаты
height = 2.8  # Высота комнаты

# 2. Стоимость покраски 1 м² стены (в рублях).
PAINT_COST_PER_SQM = 125.0

# 3. Расчёт площади пола (длина × ширина).
floor_area = length * width

# 4. Расчёт площади стен: сумма площадей 4 стен.
# Формула: 2 * (length + width) * height
walls_area = 2 * (length + width) * height

# 5. Расчёт объёма помещения.
volume = length * width * height

# 6. Расчёт общей стоимости покраски стен.
painting_cost = walls_area * PAINT_COST_PER_SQM

# 7. Форматированный вывод с округлением до 2 знаков.
print("=" * 50)
print("РАСЧЁТ ПАРАМЕТРОВ ПОМЕЩЕНИЯ")
print("=" * 50)
print(f"Размеры: {length} м x {width} м x {height} м")
print("-" * 50)
print(f"Площадь пола:        {floor_area:.2f} м²")
print(f"Площадь стен:        {walls_area:.2f} м²")
print(f"Объём помещения:     {volume:.2f} м³")
print("-" * 50)
print(f"Стоимость покраски стен: {painting_cost:.2f} руб.")
print("=" * 50)
