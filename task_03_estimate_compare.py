# Задача 3: «Конвертер температур».
# Перевод температуры из Цельсия в Фаренгейты и определение состояния воды.

# 1. Заданная температура в градусах Цельсия.
celsius = 25.0

# 2. Перевод температуры в Фаренгейты.
# Формула: F = C × 9/5 + 32
fahrenheit = celsius * 9 / 5 + 32

# 3. Определяем состояние воды в зависимости от температуры.
if celsius <= 0:
    state = "Лёд"
elif celsius < 100:
    state = "Жидкость"
else:
    state = "Пар"

# 4. Выводим результат в читаемом формате.
print("=" * 50)
print("КОНВЕРТЕР ТЕМПЕРАТУР")
print("=" * 50)
print(f"Температура в °C: {celsius}")
print(f"Температура в °F: {fahrenheit:.2f}")
print("-" * 50)
print(f"Состояние воды: {state}")
print("=" * 50)
